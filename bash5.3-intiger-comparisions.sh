# INTEGER COMPARISIONS

# EQUAL TO -eq
if [ 1 -eq 1 ]; then
    echo "1 is equal to 1"
fi

# NOT EQUAL TO -ne
if [ 1 -ne 2 ]; then
    echo "1 is not equal to 2"
fi

# LESS THAN -lt
if [ 1 -lt 2 ]; then
    echo "1 is less than 2"
fi

# LESS THAN OR EQUAL TO -le
if [ 1 -le 2 ]; then
    echo "1 is less than or equal to 2"
fi

# GREATER THAN -gt
if [ 2 -gt 1 ]; then
    echo "2 is greater than 1"
fi

# GREATER THAN OR EQUAL TO -ge
if [ 2 -ge 1 ]; then
    echo "2 is greater than or equal to 1"
fi